/**
 * 랜덤 한글 단어와 영어 번역 튜플을 반환합니다.
 */
export function getRandomWord(): [string, string] {
  const randomIndex = Math.floor(Math.random() * koreanWordPairs.length)
  return koreanWordPairs[randomIndex]
}

/**
 * 특정 길이의 랜덤 한글 단어와 영어 번역 튜플을 반환합니다.
 */
export function getRandomWordByLength(length: number): [string, string] {
  const filteredWords = koreanWordPairs.filter(([koreanWord]) => koreanWord.length === length)

  if (filteredWords.length === 0) {
    // 해당 길이의 단어가 없으면 기본 목록에서 랜덤 선택
    console.warn(`No words found with length ${length}. Returning a random word.`);
    return getRandomWord()
  }

  const randomIndex = Math.floor(Math.random() * filteredWords.length)
  return filteredWords[randomIndex]
}
